// SDV WASM Runtime - main.js
// Uses Microsoft.NET.Sdk.WebAssembly pattern (dotnet.js + getAssemblyExports)

let canvas = null;
let dotnetInstance = null;

// Register COOP/COEP service worker for SharedArrayBuffer support
if ('serviceWorker' in navigator) {
    navigator.serviceWorker.register('./coop-coep-sw.js')
        .then(() => {
            console.log("[SDV] COOP/COEP service worker registered");
            // Reload once to activate the service worker
            if (!navigator.serviceWorker.controller) {
                console.log("[SDV] Reloading to activate service worker...");
                window.location.reload();
                return;
            }
        })
        .catch(e => console.error("[SDV] Service worker registration failed:", e));
}

const SDV = {
    canvas: null,
    input: {},

    async init() {
        console.log("[SDV] Initializing runtime...");
        canvas = document.getElementById('canvas');
        if (!canvas) {
            console.error("[SDV] Canvas element not found!");
            return;
        }
        canvas.width = 1280;
        canvas.height = 720;
        SDV.canvas = canvas;
        console.log("[SDV] Canvas ready:", canvas.width, "x", canvas.height);

        // Set up input handlers
        SDV.setupInput();

        // Load .NET WASM runtime via dotnet.js (generated by SDK)
        console.log("[SDV] Loading .NET runtime...");
        try {
            const { dotnet } = await import('./_framework/dotnet.js');
            console.log("[SDV] dotnet.js loaded, creating instance...");
            dotnetInstance = await dotnet.create();
            console.log("[SDV] .NET runtime loaded");

            // Get assembly exports and call Main
            const exports = await dotnetInstance.getAssemblyExports("SdvWebPort.FnaRuntime");
            console.log("[SDV] Assembly exports:", Object.keys(exports));

            // Call Program.Main - it runs the game loop
            console.log("[SDV] Calling Program.Main...");
            const main = exports.SdvWebPort.FnaRuntime.Program.Main;
            if (main) {
                // Run Main in background (it blocks with Thread.Sleep)
                main([]);
            } else {
                console.error("[SDV] Main method not found in exports");
                console.log("[SDV] Available exports:", JSON.stringify(exports, null, 2));
            }
        } catch (e) {
            console.error("[SDV] Failed to load .NET runtime:", e);
            SDV.error("Failed to start: " + e.message + "\n" + e.stack);
        }
    },

    setupInput() {
        // Keyboard
        canvas.tabIndex = 0;
        canvas.focus();
        canvas.addEventListener('keydown', (e) => {
            SDV.input[e.code] = true;
            e.preventDefault();
        });
        canvas.addEventListener('keyup', (e) => {
            SDV.input[e.code] = false;
            e.preventDefault();
        });
        // Mouse
        canvas.addEventListener('mousedown', (e) => {
            SDV.input['mouse' + e.button] = true;
            e.preventDefault();
        });
        canvas.addEventListener('mouseup', (e) => {
            SDV.input['mouse' + e.button] = false;
            e.preventDefault();
        });
        canvas.addEventListener('mousemove', (e) => {
            const rect = canvas.getBoundingClientRect();
            SDV.mouseX = Math.round((e.clientX - rect.left) * (canvas.width / rect.width));
            SDV.mouseY = Math.round((e.clientY - rect.top) * (canvas.height / rect.height));
        });
        // Touch (mobile) - passive to avoid violation warnings
        canvas.addEventListener('touchstart', (e) => {
            const t = e.touches[0];
            const rect = canvas.getBoundingClientRect();
            SDV.mouseX = Math.round((t.clientX - rect.left) * (canvas.width / rect.width));
            SDV.mouseY = Math.round((t.clientY - rect.top) * (canvas.height / rect.height));
            SDV.input['mouse0'] = true;
        }, { passive: true });
        canvas.addEventListener('touchmove', (e) => {
            const t = e.touches[0];
            const rect = canvas.getBoundingClientRect();
            SDV.mouseX = Math.round((t.clientX - rect.left) * (canvas.width / rect.width));
            SDV.mouseY = Math.round((t.clientY - rect.top) * (canvas.height / rect.height));
        }, { passive: true });
        canvas.addEventListener('touchend', () => {
            SDV.input['mouse0'] = false;
        }, { passive: true });
        console.log("[SDV] Input handlers attached");
    },

    // Called from C# via JSImport
    onReady() {
        console.log("[SDV] C# runtime signaled ready");
        const loading = document.getElementById('loading');
        if (loading) loading.style.display = 'none';
        const status = document.getElementById('status');
        if (status) status.textContent = 'Game running';
    },

    log(msg) {
        console.log("[C#]", msg);
    },

    error(msg) {
        console.error("[C#]", msg);
        const el = document.getElementById('error-log');
        if (el) {
            el.textContent += msg + '\n';
            el.style.display = 'block';
        }
    },

    getCanvas() {
        return canvas;
    }
};

// Expose to .NET
globalThis.SDV = SDV;

// Auto-init on page load
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => SDV.init());
} else {
    SDV.init();
}
